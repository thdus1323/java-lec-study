package ex03.Exercise;

public class Exe13 {
    public static void main(String[] args) {
        double[] value = new double[100];
        int count = 0;
        for (int i = 0; i < 100; i++) {
            value[i] = 0;
            count++;
            System.out.println(value[i]);
            System.out.println(count);
        }
    }
}

